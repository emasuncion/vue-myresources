                        <p><strong>About the PDF TEXTBOOK:</strong></p>
<p>The PDF Textbook is a downloadable version of the student text, with note-taking and bookmarking enabled. The PDF Textbook can be used in class or as a reference at home.&nbsp; To use the features of the PDF Textbook, it must be downloaded to your computer or mobile device and opened in a PDF reader, such as <a href="http://www.adobe.com/go/EN_US-H-GET-READER">Acrobat 9 Reader</a> or PDF Expert.<a href="http://cambridge.edu.au/go/help/PDF_Textbook_Features.pdf"><br></a></p>
<p>Access to the PDF Textbook is included with the purchase of the printed student text and the Interactive Textbook. It is downloaded from Cambridge GO using the unique 16-character code found in the front of the printed textbook. The code will be provided in a sealed pocket or via email from your bookseller, or sent via email if purchased online.</p>
<p>&nbsp;</p>
<p><em>Subscription Terms </em></p>
<p>Access is provided to the PDF Textbook and Cambridge GO ancillary material through Cambridge GO for 500 days from the date of activation.</p>
<p>&nbsp;</p>
<p><em>Permitted uses</em></p>
<p>You are entitled to download and store PDF Textbooks on your local machine. However, they may not be transferred to another party as an electronic or print version. The downloaded PDF Textbook is expressly available for personal use only.&nbsp; Schools are entitled to load the downloaded PDFs onto their school network for use only for the same number of users as they have purchased printed or digital texts.</p>
<p><strong>&nbsp;</strong></p>
<p>&nbsp;</p>
<p><strong>About the ELECTRONIC VERSION / WORKBOOK:</strong></p>
<p>The Electronic version is a downloadable version of a print workbook, with the editing function enabled. The edit function enables students to input answers, take notes, bookmark, highlight pages and print or email answers. The Electronic Version can be used in class or as a reference at home.&nbsp; To use the features of the Electronic Version, it must be downloaded to your computer or mobile device and opened in a PDF reader, such as <a href="http://www.adobe.com/go/EN_US-H-GET-READER">Acrobat 9 Reader</a> or PDF Expert</p>
<p>The Electronic Version is available for purchase separately and is from Cambridge GO using the unique 16-character code provided in a sealed pocket or via email from your bookseller, or sent via email if purchased online.</p>
<p><em>&nbsp;</em></p>
<p><em>Subscription Terms </em></p>
<p>The licence granted for your use of <strong>the</strong> <strong>Electronic Versions</strong> on Cambridge GO is limited to access for one calendar year* unless otherwise stated on resources that have been designed to cover multiple year levels. An access code cannot be activated concurrently by multiple users.</p>
<p><em>* A calendar-year subscription is defined as follows: if activation occurs between January and July of this year, subscription concludes on the 31 December this year. If activation occurs between</em> August and December of this year, subscription concludes on 31 December the following year.</p>
<p><em>Permitted uses</em></p>
<p>You are entitled to download and store Electronic Versions on your local machine. However, they may not be transferred to another party as an electronic or print version. The downloaded Electronic Version is expressly available for personal use only.</p>
<p><em>&nbsp;</em></p>
<p><strong>About the INTERACTIVE TEXTBOOK</strong></p>
<p>The Interactive Textbook is an online version of the student text optimised to be read on screen through an internet browser.&nbsp; It delivers a host of interactive features to enhance the teaching and learning experience of the student text. The Interactive Textbook is best viewed in Firefox, Chrome or Safari.</p>
<p>The Interactive Textbook is included with the printed text, or is available for purchase separately as a digital-only option. The unique 16-character access code is found in the front of the printed textbook. If purchased as a digital-only option, the code is provided in a sealed pocket or via email from your bookseller, or sent via email if purchased online.</p>
<p><em>&nbsp;</em></p>
<p><em>Subscription Terms </em></p>
